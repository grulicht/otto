onboarding_start() {
    _print_banner

    echo "  Welcome to OTTO! Let's get you set up."
    echo ""

    # Step 1: Detect environment
    echo "  Detecting your environment..."
    local detected
    detected=$(onboarding_detect_stack)
    echo "  Found tools: ${detected}"
    echo ""

    # Step 2: Get user name
    local name
    name=$(_ask "  Your name" "${USER:-operator}")

    # Step 3: Experience level
    local level
    level=$(_ask_choice "  What is your DevOps experience level?" \
        "beginner" "intermediate" "advanced" "expert")

    # Step 4: Primary role
    local role
    role=$(_ask_choice "  What is your primary role?" \
        "developer" "sysadmin" "sre" "platform-engineer" "devops-engineer" "security" "other")

    # Step 5: Primary tools
    local tools
    tools=$(_ask_multi "  Which tools do you primarily use?" \
        "terraform" "ansible" "kubernetes" "docker" "aws" "gcp" "azure" \
        "jenkins" "gitlab-ci" "github-actions" "prometheus" "grafana" "nginx")

    # Step 6: Communication channels (simplified)
    local notifications
    notifications=$(_ask "  Enable notifications? (yes/no)" "no")

    # Step 7: Suggest profile
    local profile
    profile=$(onboarding_suggest_profile "${detected},${tools}")
    echo ""
    echo "  Suggested permission profile: ${profile}"
    local accept_profile
    accept_profile=$(_ask "  Accept this profile? (yes/no)" "yes")
    if [[ "${accept_profile}" != "yes" && "${accept_profile}" != "y" ]]; then
        profile=$(_ask_choice "  Select a profile:" \
            "general-ops" "developer" "k8s-operator" "infra-engineer" "platform-engineer" "security-auditor")
    fi

    # Step 8: Create config
    echo ""
    echo "  Creating configuration..."
    local config_path
    config_path=$(onboarding_create_config "${name}" "${level}" "${role}" "${tools}" "${profile}")

    # Set UX level
    if type ux_set_level &>/dev/null; then
        ux_set_level "${level}" > /dev/null
    fi

    # Step 9: Show quickstart
    onboarding_show_quickstart "${config_path}"

    # Offer tutorial for beginners
    if [[ "${level}" == "beginner" ]]; then
        local run_tutorial
        run_tutorial=$(_ask "  Would you like a quick tutorial? (yes/no)" "yes")
        if [[ "${run_tutorial}" == "yes" || "${run_tutorial}" == "y" ]]; then
            onboarding_interactive_tutorial
        fi
    fi

    echo "  Onboarding complete! Welcome aboard, ${name}."
    echo ""
}

# ── CLI Entry Point ──────────────────────────────────────────────────────────

if [[ "${BASH_SOURCE[0]}" == "${0}" ]]; then
    cmd="${1:-start}"
    shift || true

    case "${cmd}" in
        start)          onboarding_start ;;
        detect-stack)   onboarding_detect_stack ;;
        suggest)        onboarding_suggest_profile "$@" ;;
        create-config)  onboarding_create_config "$@" ;;
        quickstart)     onboarding_show_quickstart "$@" ;;
        tutorial)       onboarding_interactive_tutorial ;;
        help|*)
            echo "OTTO Onboarding System"
            echo ""
            echo "Usage: onboarding.sh <command> [args...]"
            echo ""
            echo "Commands:"
            echo "  start                        Run the onboarding wizard"
            echo "  detect-stack                 Detect installed tools"
            echo "  suggest <tools>              Suggest permission profile"
            echo "  create-config <args...>      Create config from parameters"
            echo "  quickstart                   Show quickstart guide"
            echo "  tutorial                     Run interactive tutorial"
            ;;
    esac
fi
