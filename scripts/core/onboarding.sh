_ask() {
    local prompt="$1"
    local default="${2:-}"
    local result

    if [[ -n "${default}" ]]; then
        printf '%s [%s]: ' "${prompt}" "${default}" >&2
    else
        printf '%s: ' "${prompt}" >&2
    fi

    read -r result
    if [[ -z "${result}" && -n "${default}" ]]; then
        result="${default}"
    fi
    echo "${result}"
}

_ask_choice() {
    local prompt="$1"
    shift
    local options=("$@")

    echo "" >&2
    echo "${prompt}" >&2
    local i=1
    for opt in "${options[@]}"; do
        printf '  %d) %s\n' "${i}" "${opt}" >&2
        i=$((i + 1))
    done
    printf 'Choice [1]: ' >&2

    local choice
    read -r choice
    choice="${choice:-1}"

    if [[ "${choice}" -ge 1 && "${choice}" -le ${#options[@]} ]] 2>/dev/null; then
        echo "${options[$((choice - 1))]}"
    else
        echo "${options[0]}"
    fi
}

_ask_multi() {
    local prompt="$1"
    shift
    local options=("$@")

    echo "" >&2
    echo "${prompt} (comma-separated numbers)" >&2
    local i=1
    for opt in "${options[@]}"; do
        printf '  %d) %s\n' "${i}" "${opt}" >&2
        i=$((i + 1))
    done
    printf 'Choices: ' >&2

    local choices_str
    read -r choices_str

    local selected=()
    IFS=',' read -ra nums <<< "${choices_str}"
    for num in "${nums[@]}"; do
        num=$(echo "${num}" | tr -d ' ')
        if [[ "${num}" -ge 1 && "${num}" -le ${#options[@]} ]] 2>/dev/null; then
            selected+=("${options[$((num - 1))]}")
        fi
    done

    if [[ ${#selected[@]} -eq 0 ]]; then
        echo "none"
    else
        local IFS=','
        echo "${selected[*]}"
    fi
}

# ── Public Functions ─────────────────────────────────────────────────────────

onboarding_detect_stack() {
    local detected=()

    local -A tool_cmds=(
        [docker]="docker"
        [kubectl]="kubectl"
        [terraform]="terraform"
        [ansible]="ansible"
        [helm]="helm"
        [git]="git"
        [aws]="aws"
        [gcloud]="gcloud"
        [az]="az"
        [vagrant]="vagrant"
        [packer]="packer"
        [nginx]="nginx"
        [python3]="python3"
        [node]="node"
        [go]="go"
        [make]="make"
        [jq]="jq"
        [curl]="curl"
        [openssl]="openssl"
    )

    for tool in "${!tool_cmds[@]}"; do
        if command -v "${tool_cmds[${tool}]}" &>/dev/null; then
            detected+=("${tool}")
        fi
    done

    # Check for config files in common locations
    if [[ -f "${HOME}/.kube/config" ]]; then
        detected+=("kubernetes-config")
    fi
    if [[ -f "${HOME}/.aws/credentials" ]]; then
        detected+=("aws-credentials")
    fi
    if [[ -f "${HOME}/.ssh/config" ]]; then
        detected+=("ssh-config")
    fi

    local IFS=','
    echo "${detected[*]}"
}

onboarding_suggest_profile() {
    local detected_tools="${1:-}"

    if [[ "${detected_tools}" == *"terraform"* && "${detected_tools}" == *"kubectl"* ]]; then
        echo "platform-engineer"
    elif [[ "${detected_tools}" == *"kubectl"* || "${detected_tools}" == *"helm"* ]]; then
        echo "k8s-operator"
    elif [[ "${detected_tools}" == *"terraform"* || "${detected_tools}" == *"ansible"* ]]; then
        echo "infra-engineer"
    elif [[ "${detected_tools}" == *"docker"* && "${detected_tools}" == *"git"* ]]; then
        echo "developer"
    else
        echo "general-ops"
    fi
}

onboarding_create_config() {
    local name="${1:-}"
    local level="${2:-beginner}"
    local role="${3:-general}"
    local tools="${4:-}"
    local profile="${5:-general-ops}"

    mkdir -p "${CONFIG_DIR}"

    cat > "${CONFIG_FILE}" <<YAML
# OTTO Configuration
# Generated by onboarding wizard on $(date -Iseconds)

user:
  name: "${name}"
  level: "${level}"
  role: "${role}"

tools:
$(echo "${tools}" | tr ',' '\n' | while read -r t; do
    t=$(echo "${t}" | tr -d ' ')
    [[ -n "${t}" ]] && echo "  - ${t}"
done)

profile: "${profile}"

preferences:
  explanations: $(if [[ "${level}" == "beginner" || "${level}" == "intermediate" ]]; then echo "true"; else echo "false"; fi)
  suggestions: $(if [[ "${level}" == "beginner" ]]; then echo "true"; else echo "false"; fi)
  tutorials: $(if [[ "${level}" == "beginner" ]]; then echo "true"; else echo "false"; fi)
  colors: true
  confirm_destructive: true

notifications:
  enabled: false
  channels: []
YAML

    echo "${CONFIG_FILE}"
}

